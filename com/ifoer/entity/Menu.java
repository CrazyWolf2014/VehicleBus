package com.ifoer.entity;

public class Menu {
    private int icon;
    private int name;
    private String text;

    public int getIcon() {
        return this.icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public String getText() {
        return this.text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public int getName() {
        return this.name;
    }

    public void setName(int name) {
        this.name = name;
    }
}
